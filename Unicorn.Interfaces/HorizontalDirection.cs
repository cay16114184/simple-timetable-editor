namespace Unicorn.Interfaces
{
    /// <summary>
    /// Defines the direction in which an object with horizontal directionality (such as a horizontal arrow) is facing or pointing.
    /// </summary>
    public enum HorizontalDirection
    {
        /// <summary>
        /// Object faces or points to the left.
        /// </summary>
        ToLeft,

        /// <summary>
        /// Object faces or points to the right.
        /// </summary>
        ToRight
    }
}
